# Variaveis
numero1 = int(input("Digite um número inteiro: "))
numero2 = int(input("Digite outro número inteiro: "))

# Operações Condicionais
print(f"Os numeros sao iguais: { numero1 == numero2 }")
print(f"Os numeros sao diferentes: { numero1 != numero2 }")
print(f"O primeiro numero é maior que o segundo: { numero1 > numero2 }")
print(f"O primeiro numero é menor que o segundo: { numero1 < numero2 }")
print(f"O primeiro numero é maior ou igual ao segundo: { numero1 >= numero2 }")
print(f"O primeiro numero é menor ou igual ao segundo: { numero1 <= numero2 }")